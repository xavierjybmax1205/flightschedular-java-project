    private void ConfirmBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmBookingActionPerformed
        // TODO add your handling code here:
        jTextArea1.setText("");
        java.sql.Timestamp currentTimestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());
        String flightName = FlightCombo.getSelectedItem().toString();
        String passengerName = PassengerCombo.getSelectedItem().toString();
        Date dateName = Date.valueOf(DateCombo.getSelectedItem().toString());
        if (flight.getSeat(flightName) != booking.getFlightSeats(flightName, dateName))
            {
                booking.addBooking(passengerName, dateName, flightName);
                jTextArea1.setText("Successfully Booked");
            }
        else
            {
                waitlist.addWaitList(flightName, dateName, passengerName,currentTimestamp);
                jTextArea1.setText("Flight is currently completely booked. Added to WaitList");
            }
    }//GEN-LAST:event_ConfirmBookingActionPerformed

