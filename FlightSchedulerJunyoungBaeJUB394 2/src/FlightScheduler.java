    private void DropConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DropConfirmActionPerformed
        // TODO add your handling code here:
        jTextArea4.setText("");
        List<String> nameDate = null;
        List<String> namePassenger = null;
        List<String> waitName = null;
        List<String> waitDate =null;
        String flightName = DropFlightCombo.getSelectedItem().toString();
        results2 = booking.getFlightStatus(flightName);
        namePassenger = results2.get(0);
        nameDate = results2.get(1);
        flight.deleteFlight(flightName);
        booking.deleteFlight(flightName);
        result3 = waitlist.getCustomerName(flightName);
        waitName = result3.get(0);
        waitDate = result3.get(1);
        waitlist.deleteFlight(flightName);
        ///readd the people into different flights
        List <String> remainingFlights = flight.getFlight();
        
        int j = 0; // counter for the index position of the day/customer list
        for (int i=0; i<remainingFlights.size(); i++)
        {
            while ((flight.getSeat(remainingFlights.get(i))) != booking.getFlightSeats(remainingFlights.get(i), Date.valueOf(nameDate.get(j))))
            {
                booking.addBooking(namePassenger.get(j), Date.valueOf(nameDate.get(j)), remainingFlights.get(i));
                jTextArea4.append(namePassenger.get(j) + " has been booked to flight " + remainingFlights.get(i) + " on " + Date.valueOf(nameDate.get(j)) +"\n");
                j ++;
                
            }
        }
        while (j< nameDate.size())
        {
            jTextArea4.append(namePassenger.get(j) + " cannot be rebooked due to flight being full on " +Date.valueOf(nameDate.get(j))+".\n");
            j++;                
        }
        for (int i =0; i< waitName.size(); i++)
        {
            jTextArea4.append(waitName.get(i) + " cancelled from waitlist date " +waitDate.get(i)+".\n");
        }
        FlightCombo.setModel(new javax.swing.DefaultComboBoxModel(flight.getFlight().toArray()));
        StatusFlightCombo.setModel(new javax.swing.DefaultComboBoxModel(flight.getFlight().toArray()));
        DropFlightCombo.setModel(new javax.swing.DefaultComboBoxModel(flight.getFlight().toArray()));
    }//GEN-LAST:event_DropConfirmActionPerformed

    private void CancelConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelConfirmActionPerformed
        // TODO add your handling code here:
        String name = CancelPassengerCombo.getSelectedItem().toString();
        String flight = null;
        String newName = null;
        String dateString = CancelCustomerDateCombo.getSelectedItem().toString();
        Date day = Date.valueOf(dateString);
        if (booking.inBooking(name, day) != null)
        {
            flight = booking.inBooking(name, day).get(0);
            booking.cancelCustomer(name, day);
            newName = waitlist.getFirstCustomer(flight, day);
            booking.addBooking(newName, day, flight);
            waitlist.cancelCustomer(newName, day);
            JOptionPane.showMessageDialog(null, "Cancel Booking Successful \n" +newName+" Is Now Booked On "+dateString, "Notice", JOptionPane.PLAIN_MESSAGE);
        
            
        }
        else if (waitlist.inWaitlist(name, day))
            {
                waitlist.cancelCustomer(name, day);
                JOptionPane.showMessageDialog(null, "Cancel WaitList Cuessful",  "Notice", JOptionPane.PLAIN_MESSAGE);
            }
        else
            JOptionPane.showMessageDialog(null, "Customer Not In Booking or Waitlist", "Notice", JOptionPane.PLAIN_MESSAGE);
        
    }//GEN-LAST:event_CancelConfirmActionPerformed

